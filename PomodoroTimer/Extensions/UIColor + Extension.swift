import UIKit

extension UIColor {
    static func mainRed() -> UIColor {
        return #colorLiteral(red: 1, green: 0.6268973351, blue: 0.5729594231, alpha: 1)
    }
    
    static func mainGreen() -> UIColor {
        return #colorLiteral(red: 0.3843137255, green: 0.7764705882, blue: 0.6431372549, alpha: 1)
    }
    
    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
    }
    
    static func mainPurple() -> UIColor {
        return #colorLiteral(red: 0.6, green: 0.2196078431, blue: 0.5568627451, alpha: 1)
    }
}
